class scenario:
    """
    A policy scenario including the following elements:
    * Carbon tax
    * CBAM
    * Final demand
    * Investment by
    * Investment converter (adjustment)
    * Supply constraints (adjustment)
    * Price change
    * Input-output coefficients (adjustment)

    Attributes
    ------
    EXOG_VARS : exog_vars
        exog_vars object containing exogenous variables

    scenario_path : str
        path to the scenario file

    Log : logging
        logging object used for writing logs

    Methods
    -------

    set_carbon_tax
        Reads out carbon tax specification from the scenario file and constructs the necessary
        long-form matrix of tax incidences. Note: the function uses a parallelized approach
        and local storage (temporary storage) when construction the matrices due to the size
        of the necessary matrices.

    carbon_tax_rate_loop(country:str)
        works together with `set_carbon_tax`, it is the internal parallel loop function for
        calculating carbon tax matrices in a parallel manner for each of the countries specified
        in the policy scenario

    set_tax_rate(carbon_tax_incidence:pd.DataFrame)
        sets the actual tax rate based on the carbon tax incidence calculated from carbon taxes



    """
    def __init__(self, EXOG_VARS:exog_vars, scenario_path:str, Log:logging):
        self.scenario_file = scenario_path
        self.scenario_data = openpyxl.load_workbook(scenario_path, read_only=True)
        # self.name = scenario_name

        self.REGIONS = EXOG_VARS.R
        self.PRODUCTS = EXOG_VARS.P
        self.IND_BASE = EXOG_VARS.IND_BASE.reset_index()

        self.HH_BASE = EXOG_VARS.HH_BASE # type: ignore
        self.GOV_BASE = EXOG_VARS.GOV_BASE # type: ignore
        self.FCF_BASE = EXOG_VARS.FCF_BASE # type: ignore

        self.regions_length = self.REGIONS.shape[0]
        self.products_length = self.PRODUCTS.shape[0]

        self.temp = temporary_storage("Temp")
        self.tax_rate = pd.DataFrame()

        self.Log = Log

        # initialize scenario values

        self.set_fd_exog()

    def set_carbon_tax(self):
        # first we read in the scenario file, the columns are set in the template
        try:
            tax_rate = pd.read_excel(self.scenario_file, sheet_name="Carbon tax", skiprows=14)
            tax_rate = tax_rate[['Country ISO*','Emitting sectors covered*','Fuel sectors covered*',
                                 'Tax value (USD / tCO2)*']]
            tax_rate.columns = ['REG_imp','PROD_COMM','TRAD_COMM','delta_tax']
            # TODO type is deprecated 
            tax_rate['type'] = 'carbon'

        except ValueError:
            # catch if there is no sheet
            # TODO type is deprecated 
            tax_rate = pd.DataFrame(columns=['REG_imp','PROD_COMM','TRAD_COMM','delta_tax','type'])

        # setting up for parallel processing
        self.tax_rate = tax_rate
        
        print("Generating carbon tax files... Parallel started.")
        # tax rate files are generated by REG_imp country
        with futures.ThreadPoolExecutor() as executor:
            results = list(tqdm(executor.map(self.carbon_tax_rate_loop, tax_rate['REG_imp'].unique()), 
                      total=len(tax_rate['REG_imp'].unique())))
            
        del results

        self.carbon_tax_countries = list(tax_rate['REG_imp'].unique())

        return True


    def carbon_tax_rate_loop(self, country:str):
        """Parallel loop function for generating tax matrices by country. Saves the tax rate
        matrices in file.

        Parameters
        ----------
        country : str
            country to generate carbon tax file for

        Returns
        -------
        bool
            Returns True if completed
        """
          
        # first retrieve the tax rate file from the class
        tax_rate = self.tax_rate

        # TODO REG_exp is ALL for now
        tax_rate['REG_exp'] = 'ALL'
        tax_rate_country = tax_rate[tax_rate['REG_imp']==country].copy()

        # process flows_table, for simplicity the flows table (excel) allows flows to
        # be allocated to multiple sectors either in hyphen e.g. 10-30 or comma e.g.
        # 10,11,15 format, these are parsed to actual sectors here

        tax_rate_country = resolve_hyphen(tax_rate_country, 'PROD_COMM')
        tax_rate_country = resolve_comma(tax_rate_country, 'PROD_COMM')
        tax_rate_country = resolve_hyphen(tax_rate_country, 'TRAD_COMM')
        tax_rate_country = resolve_comma(tax_rate_country, 'TRAD_COMM')

        tax_rate_country = resolve_comma(tax_rate_country, 'REG_imp')

        # treatment of ALL in PROD_COMM, ie all upstream sectors impacted
        # ! FD_2 to FD_6 is missing!!!!!
        tax_rate_country = resolve_all(tax_rate_country, 'PROD_COMM', [x for x in np.arange(1,121)] + ['FD_1'])
        tax_rate_country = resolve_all(tax_rate_country, 'TRAD_COMM', [x for x in np.arange(1,121)])
        tax_rate_country = resolve_all(tax_rate_country, 'REG_exp', self.REGIONS['Region_acronyms'].to_list())
        tax_rate_country = resolve_all(tax_rate_country, 'REG_imp', self.REGIONS['Region_acronyms'].to_list())

        tax_rate_country = tax_rate_country.rename(columns={'delta_tax':'ctax'})
        self.temp.save_to_pickle(tax_rate_country, "{}_carbon_tax_rate".format(country))

        return True
    
