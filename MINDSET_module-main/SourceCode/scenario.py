    def set_fd_exog(self):
        # columns to be passed on
        columns_ = ['REG_exp','REG_imp','TRAD_COMM','PROD_COMM','Value','Type']

        try:
            fd_exog = pd.read_excel(self.scenario_file, sheet_name='Final demand', skiprows=14)
            fd_exog = fd_exog[['Producing country ISO*','Consuming country ISO*','Product code*',
                                 'FD code*','Value*','Type*']]
            fd_exog.columns = columns_
        except (ValueError, KeyError):
            fd_exog = pd.DataFrame(columns=columns_)

        # add index before resolving
        fd_exog['Line'] = np.arange(len(fd_exog)).tolist()

        fd_exog = resolve_comma(fd_exog, "REG_imp")
        fd_exog = resolve_all(fd_exog, "REG_imp", self.REGIONS['Region_acronyms'].to_list())

        fd_exog = resolve_comma(fd_exog, "REG_exp")
        fd_exog = resolve_all(fd_exog, "REG_exp", self.REGIONS['Region_acronyms'].to_list())

        fd_exog = resolve_comma(fd_exog, "TRAD_COMM")
        fd_exog = resolve_hyphen(fd_exog, "TRAD_COMM")
        fd_exog = resolve_all(fd_exog, "TRAD_COMM", [str(x) for x in np.arange(1,121)])

        fd_exog = resolve_comma(fd_exog, "PROD_COMM")
        fd_exog = resolve_all(fd_exog, "PROD_COMM", ['FD_1','FD_3','FD_4'])

        # ? Scenario.fd_exog structure is the following:
        # ? REG_imp | TRAD_COMM | PROD_COMM | Value | Type
        # ? such as PROD_COMM can only by FD_1, FD_2 ... FD_06
        # ? Type can be: abs-b [absolute-proportional-base] | abs [absolute-proportional-new] 
        # ? | rel [relative] | rel-b [relative to baseline]
        # ? if absolute is used with multiple sectors / countries
        # ? then numbers are proportionally spread out
        # ? proportionality is either to the base [abs-b] or to the current FD vector [abs]
        # TODO: abs is to be implemented later [with annualized model version]
        # TODO: rel is to be implemented later [with annualized model version]

        # WE CALCULATE FD_EXOG types here, bc the scenario class
        # should have direct impacts

        # ! INV is missing [FD_5 - FD_6]
        # ! NPISH is missing [FD_2]

        fd_01_initial = self.HH_BASE.reset_index().rename(columns={'VIPA':'FD_1'})
        fd_03_initial = self.GOV_BASE.reset_index().rename(columns={'VIGA':'FD_3'})
        fd_04_initial = self.FCF_BASE.reset_index().rename(columns={'VDFA':'FD_4'})
        # | REG_exp | REG_imp | TRAD_COMM |

        fd_initial = fd_01_initial.merge(fd_03_initial, how="outer").merge(fd_04_initial, how="outer")
        fd_initial = pd.melt(fd_initial, id_vars=['REG_exp','REG_imp','TRAD_COMM'],
                              var_name='PROD_COMM', value_name='y0')
        fd_initial['y0'] = fd_initial['y0'].fillna(0)
        del fd_01_initial, fd_03_initial, fd_04_initial
        # | REG_imp | REG_exp | TRAD_COMM | PROD_COMM [FD_1-FD_4] | y0 #

        fd_initial = fd_initial.astype({'TRAD_COMM':'str'})
        fd_exog = fd_exog.merge(fd_initial, how='left', on=['REG_imp','REG_exp','TRAD_COMM','PROD_COMM'])
        fd_exog['dy'] = 0.0

        # assigned vars dict
        variables = {}
        # ? assign
        df_assign = fd_exog[fd_exog['Type'].str.contains('assign')].copy()

        for i, r in df_assign.iterrows():
            name = r['Value'].replace("[TRAD_COMM]",str(r['TRAD_COMM'])).replace("[REG_imp]",str(r['REG_imp'])).replace("[REG_exp]",str(r['REG_exp'])).replace("[PROD_COMM]",str(r['PROD_COMM']))
            match_ = (df_assign['REG_imp']==r['REG_imp']) & (df_assign['TRAD_COMM']==r['TRAD_COMM']) & (df_assign['PROD_COMM']==r['PROD_COMM']) & (df_assign['REG_exp']==r['REG_exp'])
            variables[name] = variables[name] + df_assign.loc[match_, 'y0'] #type: ignore

        variables_df = pd.DataFrame({"index": variables.keys(), "value": [x.values[0] if len(x)!=0 else 0 for x in variables.values()]})

        # ? eval
        # ? eval can access both variables_df and fd_exog
        df_eval = fd_exog[fd_exog['Type'].str.contains('eval')].copy()

        for i, r in df_eval.iterrows():
            match_ = (df_eval['REG_imp']==r['REG_imp']) & (df_eval['TRAD_COMM']==r['TRAD_COMM']) & (df_assign['PROD_COMM']==r['PROD_COMM']) & (df_assign['REG_exp']==r['REG_exp'])
            exp = r['Value'].replace("[PROD_COMM]",str(r['PROD_COMM'])).replace("[TRAD_COMM]",str(r['TRAD_COMM'])).replace("[REG_imp]",str(r['REG_imp'])).replace("[REG_exp]",str(r['REG_exp']))
            df_eval.loc[match_, 'dy'] = eval(exp)

        # ? abs-b: abs-b is absolute impact based on Y0
        # ? if multiple specified from something, then spread out proportionally

        df_absb = fd_exog[fd_exog['Type'].str.contains('abs-b')].copy()
        # calculate share by line
        df_absb['share'] = df_absb['y0'] / df_absb.groupby(['Line'])['y0'].transform('sum')
        df_absb['dy'] = df_absb['Value'] * df_absb['share']
        # ? rel-b

        df_relb = fd_exog[fd_exog['Type'].str.contains('rel-b')].copy()
        df_relb['dy'] = df_relb['y0'] * df_relb['Value']

        # ! LINES either overwrite each other (last one stays) or appended (sum)
        # ! only SUM for now!

        df_impacts = pd.concat([df_eval, df_absb, df_relb])
        self.Log.log_to_csv("fd_exog_", df_impacts)
        # ? REG_imp | REG_exp | TRAD_COMM | PROD_COMM [FD_1] | dy
        df_impacts = df_impacts.drop(columns=['Value','y0','Line'])
        df_impacts = df_impacts.groupby(['REG_exp','REG_imp','TRAD_COMM','PROD_COMM']).agg({'dy':'sum'}).reset_index()
        self.fd_exog = df_impacts

        return True

