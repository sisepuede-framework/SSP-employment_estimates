    def set_io_changes(self):
        try:
            io_changes = pd.read_excel(self.scenario_file, sheet_name="Input-output coefficients", skiprows=14)
            io_changes = io_changes[['Origin country ISO*','Target country ISO*','Origin sector code*','Target sector code*','Value*','Type*']]
            io_changes.columns = ['REG_exp','REG_imp','TRAD_COMM','PROD_COMM','Value','Type']
            
            # index
            io_changes['line'] = range(len(io_changes))
            # resolve hyphen
            io_changes = resolve_hyphen(io_changes, 'TRAD_COMM')
            io_changes = resolve_hyphen(io_changes, 'PROD_COMM')
            io_changes = resolve_all(io_changes, 'TRAD_COMM', [x for x in np.arange(1,121)])
            io_changes = resolve_all(io_changes, 'PROD_COMM', [x for x in np.arange(1,121)])
            
            # Save inputs for Specific country
            io_changes_specific = io_changes[(io_changes['REG_exp']!='ALL') & (io_changes['REG_imp']!='ALL')].copy()
            # treatment of ALL in REG_exp
            pairs = io_changes[io_changes['REG_exp']=='ALL'][['REG_imp','PROD_COMM','TRAD_COMM','Value','Type','line']].copy()
            if len(pairs) > 0:
                pairs['key'] = 1
                keys = pd.DataFrame({
                    'REG_exp': self.REGIONS['Region_acronyms'].to_list(),
                    'key': 1
                })
                    
                complement_ALL = keys.merge(pairs, how='left', on='key').drop(columns=['key'])
                io_changes = pd.concat([io_changes[io_changes['REG_exp']!='ALL'], complement_ALL])
                
                del complement_ALL
            # treatment of ALL in REG_imp
            pairs = io_changes[io_changes['REG_imp']=='ALL'][['REG_exp','PROD_COMM','TRAD_COMM','Value','Type','line']].copy()
            if len(pairs) > 0:
                pairs['key'] = 1
                keys = pd.DataFrame({
                    'REG_imp': self.REGIONS['Region_acronyms'].to_list(),
                    'key': 1
                })
                complement_ALL = keys.merge(pairs, how='left', on='key').drop(columns=['key'])
    
                io_changes = pd.concat([io_changes[io_changes['REG_imp']!='ALL'], complement_ALL])
    
                del complement_ALL
                    
            # treatment of KEEP in REG_exp
               # total of input needs to be as specified, but keep distribution across REG_exp
            pairs = io_changes[io_changes['REG_exp']=='KEEP'][['REG_imp','PROD_COMM','TRAD_COMM','Value','Type','line']].copy()
            if len(pairs) > 0:
                # TODO: for now this is okay, but if we have more lines of scenario this might gonna cause issues
                pairs['key'] = 1
                keys = pd.DataFrame({
                    'REG_exp': self.REGIONS['Region_acronyms'].to_list(),
                    'key': 1
                })
    
                complement_ALL = keys.merge(pairs, how='left', on='key').drop(columns=['key'])
                # now we need to recalculate the relevant shares
                # complement_ALL is
                # REG_exp, REG_imp, PROD_COMM, TRAD_COMM, Value'
                complement_ALL = (complement_ALL.astype({'TRAD_COMM':'int','PROD_COMM':'int'})).merge(self.IND_BASE[['REG_imp','REG_exp','PROD_COMM','TRAD_COMM','z_bp']], 
                                     how='left', on=['REG_imp','REG_exp','PROD_COMM','TRAD_COMM'])
                # calculate relevant share
                complement_ALL['REG_exp_share'] = \
                    complement_ALL['z_bp'] / complement_ALL.groupby(['REG_imp','PROD_COMM','TRAD_COMM'])['z_bp'].transform('sum')
                complement_ALL['Value'] = complement_ALL['Value'] * complement_ALL['REG_exp_share']
                complement_ALL = complement_ALL.drop(columns=['z_bp','REG_exp_share'])
                io_changes = pd.concat([io_changes[io_changes['REG_exp']!='KEEP'], complement_ALL])
                del complement_ALL
                
            # we might end up with duplicates, the latest assignment is the valid 
            io_changes = io_changes.sort_values(by='line').drop_duplicates(keep='last', subset=['REG_imp','REG_exp','PROD_COMM','TRAD_COMM']).drop(columns=['line'])

        except ValueError:
            io_changes = pd.DataFrame(columns=['REG_imp','PROD_COMM','REG_exp','TRAD_COMM','Type','Value'])
            
        self.io_changes = io_changes

        return io_changes

