        a1 = len(df_inv_conv)

        df_inv_conv = pd.concat([df_inv_conv for x in range(len(self.REGIONS))])
        df_inv_conv['REG_imp'] = np.repeat(self.REGIONS['Region_acronyms'], a1).tolist()

        if len(inv_conv_adj) > 0:

            inv_conv_adj = resolve_comma(inv_conv_adj, "REG_imp")
            inv_conv_adj = resolve_all(inv_conv_adj, "REG_imp", self.REGIONS['Region_acronyms'].to_list())

            inv_conv_adj = resolve_comma(inv_conv_adj, "PROD_COMM")
            inv_conv_adj = resolve_hyphen(inv_conv_adj, "PROD_COMM")
            inv_conv_adj = resolve_all(inv_conv_adj, "PROD_COMM", [x for x in np.arange(1,121)])

            inv_conv_adj = resolve_comma(inv_conv_adj, "TRAD_COMM")
            inv_conv_adj = resolve_hyphen(inv_conv_adj, "TRAD_COMM")
            inv_conv_adj = resolve_all(inv_conv_adj, "TRAD_COMM", [x for x in np.arange(1,121)])

            # ? Type can be:
            # ? abs-b | rel-b | abs | rel | replace
            # TODO: abs is to be implemented later
            # TODO: abs is to be implemented later
            # TODO: abs-b is to be implemented later [with annualized model version]
            # TODO: rel-b is to be implemented later [with annualized model version]

            # ! NOTE: with this setup there is no differentiation in terms of investment conversion
            # ! between INDUCED | RECYCLED | EXOG investment types
            # TODO: might need to change this

            # ? replace: replace is direct replacement of INV_CONV coeffs

            df_replace = inv_conv_adj[inv_conv_adj['Type'].str.contains('replace')].copy()
            # calculate 
            df_inv_conv = df_inv_conv.astype({'TRAD_COMM':'str'})
            df_inv_conv = df_inv_conv.merge(df_replace, how='outer', on=['REG_imp','PROD_COMM','TRAD_COMM'])
